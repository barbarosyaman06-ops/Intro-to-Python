#= is an assingment operator in python. It is used to assign a value to a variable.

#+= is an assignment operator which adds the right operand to the left operand.
x=5
x+=3
print(x) # Output: 8

y=6
y-=2
print(y) # Output: 4

z=7
z*=2
print(z) #output: 14

k=5
k%=2
print(k) #output: 1

l=8
l//=3
print(l) #output: 2

m=2
m**=3
print(m) #output: 8
